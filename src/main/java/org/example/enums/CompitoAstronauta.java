package org.example.enums;

/**
 * Rappresenta i ruoli che un astronauta può avere all'interno dell'equipaggio.
 * Ogni ruolo corrisponde a una specifica responsabilità o area di competenza.
 */
public enum CompitoAstronauta {
    /**
     * Il pilota è responsabile del controllo e della navigazione della navicella spaziale.
     */
    PILOTA,

    /**
     * Il co-pilota assiste il pilota nelle operazioni di volo e può assumere il controllo
     * della navicella in caso di necessità.
     */
    CO_PILOTA,

    /**
     * Il medico si occupa della salute e del benessere dell'equipaggio durante la missione.
     */
    MEDICO,

    /**
     * Il biologo conduce esperimenti e ricerche sulla vita e sugli organismi nello spazio.
     */
    BIOLOGO,

    /**
     * Il fisico studia i fenomeni fisici e le leggi della natura nello spazio.
     */
    FISICO,

    /**
     * L'ingegnere è responsabile del funzionamento e della manutenzione dei sistemi
     * tecnici della navicella spaziale.
     */
    INGEGNERE
}