package org.example.enums;

/**
 * The AgenziaSpaziale enum represents various space agencies around the world.
 * It includes the following space agencies:
 * <ul>
 *   <li>NASA - National Aeronautics and Space Administration (United States)</li>
 *   <li>RKA - Russian Space Agency (Russia)</li>
 *   <li>ESA - European Space Agency (Europe)</li>
 *   <li>JAXA - Japan Aerospace Exploration Agency (Japan)</li>
 *   <li>CSA_ASC - Canadian Space Agency (Canada)</li>
 * </ul>
 */
public enum AgenziaSpaziale {
    /**
     * National Aeronautics and Space Administration (United States)
     */
    NASA,

    /**
     * Russian Space Agency (Russia)
     */
    RKA,

    /**
     * European Space Agency (Europe)
     */
    ESA,

    /**
     * Japan Aerospace Exploration Agency (Japan)
     */
    JAXA,

    /**
     * Canadian Space Agency (Canada)
     */
    CSA_ASC
}